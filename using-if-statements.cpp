#include <iostream>
using std::cin;
using std::cout;

int main() {
   cout << "Enter current time in Army Timing system:\n";
   int time;
   cin >> time;
   
if (time>0400 && time<1200){
    cout << "Good Morning!";
}
else if (time>1200 && time<1800){
    cout << "Good Afternoon!";
}
else if (time>1800 && time<2200){
    cout << "Good Evening!";
}
else if (time>2200 or time<0400){
    cout << "Good Night!";
}
    return 0;
}
